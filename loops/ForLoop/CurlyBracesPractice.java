package loops.ForLoop;

public class CurlyBracesPractice {
    public static void main(String[] args) {
        for( int a=0;a<10;a++)
            System.out.println("Winter");//like if its true but just for 1 line include for

        System.out.println("Summer");
        int c=0;
        while(c<10)
            System.out.println("Winter");//like if its true but just for 1 line include for
            c++;                         //infinitive loops
        System.out.println("Summer");
    }
}
